export type Dict = typeof vi;

/**
 * Dot-paths to every string leaf in the dictionary — the set of legal `t()`
 * keys. Gives autocomplete and compile-time key checking.
 */
export type TranslationKey = DeepKey<Dict>;

type DeepKey<T, Prefix extends string = ""> = T extends string
  ? Prefix
  : T extends Record<string, unknown>
    ? {
        [K in keyof T & string]: DeepKey<
          T[K],
          Prefix extends "" ? K : `${Prefix}.${K}`
        >;
      }[keyof T & string]
    : never;

